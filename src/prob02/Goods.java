package prob02;

public class Goods {
	
	private String juice;
	private int price;
	private int num;
	public Goods(String juice, int price, int num) {

		this.juice = juice;
		this.price = price;
		this.num = num;
	}
	public String getJuice() {
		return juice;
	}
	public void setJuice(String juice) {
		this.juice = juice;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
	
	
	
}
